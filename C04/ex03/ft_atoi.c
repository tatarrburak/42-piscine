int	ft_atoi(char *str)
{
	int	i;
	int	say;
	int	is;

	is = 1;
	say = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			is *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		say = (say * 10) + (str[i] - 48);
		i++;
	}
	say *= is;
	return (say);
}
